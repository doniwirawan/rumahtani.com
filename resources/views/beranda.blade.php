<div class="about p-3 mt-4 ">

    <h1 class="purples ">Apa Itu SDGs ? </h1>

    <p class="blacks text-capitalize mt-4">Sustainable Development Goals (SDGs) merupakan suatu rencana aksi
        global yang disepakati oleh para pemimpin dunia,
        termasuk Indonesia, guna mengakhiri kemiskinan, mengurangi kesenjangan dan melindungi lingkungan. SDGs
        berisi 17 Tujuan
        dan 169 Target yang diharapkan dapat dicapai pada tahun 2030.
    </p>
    <p>
        25 September 2015 bertempat di Markas Besar Perserikatan Bangsa-Bangsa (PBB), para pemimpin dunia secara
        resmi
        mengesahkan Agenda Tujuan Pembangunan Berkelanjutan (Sustainable Development Goals) sebagai kesepakatan
        pembangunan
        global. Kurang lebih 193 kepala negara hadir, termasuk Wakil Presiden Indonesia Jusuf Kalla turut
        mengesahkan Agenda
        SDGs.
    </p>
    <p>
        Dengan mengusung tema "Mengubah Dunia Kita: Agenda 2030 untuk Pembangunan Berkelanjutan", SDGs yang
        berisi 17 Tujuan dan
        169 Target merupakan rencana aksi global untuk 15 tahun ke depan (berlaku sejak 2016 hingga 2030), guna
        mengakhiri
        kemiskinan, mengurangi kesenjangan dan melindungi lingkungan. SDGs berlaku bagi seluruh negara
        (universal), sehingga
        seluruh negara tanpa kecuali negara maju memiliki kewajiban moral untuk mencapai Tujuan dan Target SDGs.
    </p>
    <h2 class="h4 purples">
        SDGs Dirancang Secara Partisipatif
    </h2>
    <p>
        Berbeda dari pendahulunya Millenium Development Goals (MDGs), SDGs dirancang dengan melibatkan seluruh
        aktor
        pembangunan, baik itu Pemerintah, Civil Society Organization (CSO), sektor swasta, akademisi, dan
        sebagainya. Kurang
        lebih 8,5 juta suara warga di seluruh dunia juga berkontribusi terhadap Tujuan dan Target SDGs.
    </p>
    <h2 class="h4 purples">
        Tidak Meninggalkan Satu Orangpun (Leave No One Behind)
    </h2>
    <p>
        Tidak Meninggalkan Satu Orangpun merupakan Prinsip utama SDGs. Dengan prinsip tersebut setidaknya SDGs
        harus bisa
        menjawab dua hal yaitu, Keadilan Prosedural yaitu sejauh mana seluruh pihak terutama yang selama ini
        tertinggal dapat
        terlibat dalam keseluruhan proses pembangunan dan Keadilan Subtansial yaitu sejauh mana kebijakan dan
        program
